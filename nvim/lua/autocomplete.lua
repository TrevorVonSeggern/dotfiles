  sync_install = true,
  auto_install = true,
  ignore_install = { "javascript" },
  highlight = {
    -- `false` will disable the whole extension
    enable = true,
    --disable = { "c", "rust" },
    additional_vim_regex_highlighting = false,
  },
}
